# operadores matematicos
num1 = 5
num2 = 8

# Atribui total da operacao matematica
soma = num1 + num2          # Soma (+)
subtrai = num1 - num2       # Subtracao (-)
dividefloat = num1 / num2   # Divisao (/)
divideint = num1 // num2    # Divisao inteiro (//)
multiplica = num1 * num2    # Multiplicacao (*)
resto = num1 % num2         # Resto da divisao (mod) (%)
potencia = num1**num2       # Potencia (**)

# Exibe total da operacao matematica
print("\n")
print("Soma num1 + num2:",soma)
print("Subtracao de num1 - num2:",subtrai)
print("Divisao de num1 / num2:",dividefloat)
print("Divisao inteira de num1 // num2:",divideint)
print("Multiplica num1 * num2:",multiplica)
print("Resto da dividao de num1 % num2:",resto)
print("Potencia de num1 ** num2:",potencia)
print("\n")


# Operadores booleanos
print("Compara num 1 > num2:", num1 > num2)
print("Compara num 1 >= num2:", num1 >= num2)
print("Compara num 1 < num2:", num1 < num2)
print("Compara num 1 <= num2:", num1 <= num2)
print("Compara strings:igual", "num1" == "num2")
